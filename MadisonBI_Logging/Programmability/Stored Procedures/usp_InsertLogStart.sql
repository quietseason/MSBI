CREATE PROCEDURE [dbo].[usp_InsertLogStart]
	@ETL_PACKAGE_ID INT,
	@MACHINE_NAME NVARCHAR(255),
	@EXECUTE_USER NVARCHAR(100),
	@START_TIME DATETIME,
	@ETL_PROCESS_LOG_ID INT OUTPUT
AS
BEGIN
	INSERT dbo.ETL_PROCESS_LOG(ETL_PACKAGE_ID,MACHINE_NAME,EXECUTE_USER,START_TIME,ETL_STATUS_ID)
	VALUES(@ETL_PACKAGE_ID,@MACHINE_NAME,@EXECUTE_USER,@START_TIME,1);
	SELECT @ETL_PROCESS_LOG_ID = @@IDENTITY;
END
